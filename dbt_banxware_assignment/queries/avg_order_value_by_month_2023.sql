-- What is the average order value for each month in the year 2023?

SELECT 
    MONTH, 
    AVG(TOTAL_SALES_AMOUNT) AS AVG_ORDER_VALUE
FROM HOME_ASSIGNMENT.PUBLIC.TRANSFORMED_SALES_DATA
WHERE YEAR = 2023
GROUP BY MONTH
ORDER BY MONTH;

